--CREATION

CREATE TABLE DIRECTOR (
  DIRECTOR_ID NUMBER PRIMARY KEY,
  D_NAME VARCHAR(20) NOT NULL,
  DOB DATE
);

CREATE TABLE SHOWS (
  ID NUMBER PRIMARY KEY,
  SHOW_TYPE VARCHAR(20) NOT NULL,
  SHOW_NAME VARCHAR(20) NOT NULL,
  RATING NUMBER(2),
  SYNOPSIS VARCHAR(100) 
);

CREATE TABLE MOVIE (
  ID NUMBER PRIMARY KEY,
  BOX_OFFICE_COLLECTION NUMBER,
  RELEASE_DATE DATE,
  DIRECTOR_ID NUMBER NOT NULL,
  FOREIGN KEY (DIRECTOR_ID) REFERENCES DIRECTOR,
  FOREIGN KEY (ID) REFERENCES SHOWS
);

CREATE TABLE TVSHOW (
  ID NUMBER PRIMARY KEY,
  EP_COUNT NUMBER NOT NULL,
  S_COUNT NUMBER NOT NULL,
  FOREIGN KEY (ID) REFERENCES SHOWS
);

CREATE TABLE COMPANY (
  C_NAME VARCHAR(20) PRIMARY KEY,
  YEAR_FOUNDED NUMBER,
  WEB_ADDRESS VARCHAR(100) NOT NULL
);

CREATE TABLE COMPANY_PHONE (
  C_NAME VARCHAR(20),
  PHONE NUMBER(10) NOT NULL,
  PRIMARY KEY (C_NAME, PHONE),
  FOREIGN KEY (C_NAME) REFERENCES COMPANY
);

CREATE TABLE PRODUCED_BY (
  ID NUMBER,
  C_NAME VARCHAR(20),
  PRIMARY KEY (ID, C_NAME),
  FOREIGN KEY (ID) REFERENCES SHOWS,
  FOREIGN KEY (C_NAME) REFERENCES COMPANY
);

CREATE TABLE GENRE (
  GENRE_ID NUMBER PRIMARY KEY,
  GENRE_NAME VARCHAR(20) NOT NULL
);

CREATE TABLE HAS_GENRE (
  ID NUMBER,
  GENRE_ID NUMBER,
  PRIMARY KEY (ID, GENRE_ID),
  FOREIGN KEY (ID) REFERENCES SHOWS,
  FOREIGN KEY (GENRE_ID) REFERENCES GENRE
);

CREATE TABLE STREAMING_PARTNER (
  S_ID NUMBER PRIMARY KEY,
  S_NAME VARCHAR(20) NOT NULL,
  SUBSCRIPTION_FEES NUMBER,
  LINK VARCHAR(100) NOT NULL
);

CREATE TABLE AVAILABLE_ON (
  ID NUMBER,
  S_ID NUMBER,
  PRIMARY KEY (ID, S_ID),
  FOREIGN KEY (ID) REFERENCES SHOWS,
  FOREIGN KEY (S_ID) REFERENCES STREAMING_PARTNER
);

CREATE TABLE ACTOR (
  ACTOR_ID NUMBER PRIMARY KEY,
  FIRST_NAME VARCHAR(20) NOT NULL,
  LAST_NAME VARCHAR(20) NOT NULL,
  DOB DATE,
  HEIGHT FLOAT
);

CREATE TABLE THE_CAST (
  ID NUMBER,
  ACTOR_ID NUMBER,
  SALARY NUMBER,
  PRIMARY KEY (ID, ACTOR_ID),
  FOREIGN KEY (ID) REFERENCES SHOWS,
  FOREIGN KEY (ACTOR_ID) REFERENCES ACTOR
);

CREATE TABLE AWARD (
  AWARD_ID NUMBER PRIMARY KEY,
  AWARD_NAME VARCHAR(20) NOT NULL,
  PRIZE NUMBER NOT NULL
);

CREATE TABLE WON (
  ID NUMBER,
  AWARD_ID NUMBER,
  WON_DATE DATE NOT NULL,
  PRIMARY KEY (ID, AWARD_ID),
  FOREIGN KEY (ID) REFERENCES SHOWS,
  FOREIGN KEY (AWARD_ID) REFERENCES AWARD
);

CREATE TABLE REVIEWER (
  USERID NUMBER PRIMARY KEY,
  USERNAME VARCHAR(20) NOT NULL
);

CREATE TABLE REVIEWED_BY (
  USERID NUMBER,
  ID NUMBER,
  REVIEW VARCHAR(50) NOT NULL,
  PRIMARY KEY (ID, USERID),
  FOREIGN KEY (ID) REFERENCES SHOWS,
  FOREIGN KEY (USERID) REFERENCES REVIEWER
);

CREATE TABLE SONG (
  SONG_TITLE VARCHAR(20),
  ID NUMBER,
  SONG_LENGTH NUMBER NOT NULL,
  SINGER VARCHAR(20),
  PRIMARY KEY (SONG_TITLE, ID),
  FOREIGN KEY (ID) REFERENCES MOVIE
);

CREATE TABLE EPISODE (
  EP_NO NUMBER,
  S_NO NUMBER,
  ID NUMBER,
  EP_TITLE VARCHAR(20) NOT NULL,
  EP_LENGTH NUMBER NOT NULL,
  RELEASE_DATE DATE NOT NULL,
  DIRECTOR_ID NUMBER NOT NULL,
  PRIMARY KEY (EP_NO, S_NO, ID),
  FOREIGN KEY (DIRECTOR_ID) REFERENCES DIRECTOR,
  FOREIGN KEY (ID) REFERENCES TVSHOW
);

